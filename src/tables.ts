// YM2149 master clock frequency (Atari ST)
export const MASTER_CLOCK = 2_000_000;

// Internal clock divider
export const CLOCK_DIVIDER = 8;

// Internal clock rate
export const INTERNAL_CLOCK = MASTER_CLOCK / CLOCK_DIVIDER; // 250kHz

// Tone period to frequency conversion
// freq = masterClock / (16 * period) = internalClock / (2 * period)
export function periodToFrequency(period: number): number {
  if (period <= 0) return 0;
  return INTERNAL_CLOCK / (2 * period);
}

// Frequency to period conversion (for reference)
export function frequencyToPeriod(freq: number): number {
  if (freq <= 0) return 4095;
  return Math.round(INTERNAL_CLOCK / (2 * freq));
}

